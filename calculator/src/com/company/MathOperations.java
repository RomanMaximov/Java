package com.company;

public class MathOperations {
    public String plus(int val1, int val2)
    {
        return Integer.toString(val1 + val2);
    }

    public String minus(int val1, int val2)
    {
        return Integer.toString(val1 - val2);
    }

    public String multiply(int val1, int val2)
    {
        return Integer.toString(val1 * val2);
    }

    public String devide(int val1, int val2)
    {
        return Integer.toString(val1 / val2);
    }
}
